    <li><strong>Comunicaciones</strong>: enviarte notificaciones sobre tus mensajes o actividad en la plataforma (interés legítimo).</li>
    <li><strong>Analítica</strong>: medir el uso de la web mediante Google Analytics 4 (consentimiento).</li>
</ul>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">Conservación</h4>
<p>Tus datos se conservan mientras mantengas tu cuenta activa. Al eliminar la cuenta, los datos se borran en un plazo máximo de 30 días salvo obligación legal en contrario.</p>

