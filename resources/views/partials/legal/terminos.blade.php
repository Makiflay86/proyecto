<h3 class="text-base font-semibold text-gray-900 dark:text-white mb-3">Términos y Condiciones</h3>

<p class="mb-3">Al usar Venalia aceptas los presentes términos. Si no estás de acuerdo, por favor, no uses la plataforma.</p>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">1. Descripción del servicio</h4>
<p>Venalia es una plataforma de anuncios clasificados que permite a particulares publicar y buscar productos de segunda mano. No intervenimos en las transacciones ni somos responsables del resultado de las mismas.</p>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">2. Registro y cuenta de usuario</h4>
<ul class="list-disc list-inside space-y-1">
    <li>Debes ser mayor de 16 años para registrarte.</li>
    <li>Eres responsable de mantener la confidencialidad de tu contraseña.</li>
    <li>Está prohibido crear cuentas falsas o hacerse pasar por otra persona.</li>
</ul>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">3. Publicación de productos</h4>
<ul class="list-disc list-inside space-y-1">
    <li>Solo puedes publicar artículos de tu propiedad o que tengas autorización para vender.</li>
    <li>Queda prohibida la venta de artículos ilegales, falsificados o que vulneren derechos de terceros.</li>
    <li>Las imágenes y descripciones deben ser verídicas y corresponder al artículo real.</li>
    <li>Nos reservamos el derecho a eliminar publicaciones que incumplan estas normas.</li>
</ul>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">4. Uso del chat</h4>
<p>El sistema de mensajería interno es exclusivo para coordinar compraventas. Queda prohibido su uso para spam, acoso o cualquier comunicación no relacionada con los productos.</p>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">5. Limitación de responsabilidad</h4>
<p>Venalia actúa como tablón de anuncios. No garantizamos la veracidad de las publicaciones ni nos hacemos responsables de pérdidas derivadas de transacciones entre usuarios.</p>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">6. Modificaciones</h4>
<p>Podemos actualizar estos términos en cualquier momento. Los cambios se comunicarán a través de la plataforma y el uso continuado implica su aceptación.</p>
