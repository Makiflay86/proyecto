<h3 class="text-base font-semibold text-gray-900 dark:text-white mb-3">Aviso Legal</h3>

<p class="mb-3">En cumplimiento del artículo 10 de la Ley 34/2002, de 11 de julio, de Servicios de la Sociedad de la Información y Comercio Electrónico (LSSICE), se facilita la siguiente información:</p>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">Titular del sitio web</h4>
<p>Venalia (en adelante, "la Plataforma") es un marketplace de compraventa de segunda mano operado como proyecto personal/educativo.</p>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">Objeto</h4>
<p>La Plataforma facilita el contacto entre particulares para la compraventa de productos de segunda mano. Venalia no es parte en las transacciones entre usuarios y no asume responsabilidad alguna sobre el estado, descripción o precio de los artículos publicados.</p>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">Propiedad intelectual</h4>
<p>Los textos, imágenes, logotipos y demás elementos de esta web son propiedad de sus respectivos autores. Queda prohibida su reproducción total o parcial sin autorización expresa.</p>

<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">Ley aplicable</h4>
<p>Las presentes condiciones se rigen por la legislación española. Para cualquier controversia, las partes se someten a los juzgados y tribunales del domicilio del usuario.</p>
