<h4 class="font-semibold text-gray-800 dark:text-gray-200 mt-4 mb-1">Cómo gestionar tus cookies</h4>
<p>Puedes retirar tu consentimiento en cualquier momento borrando el elemento <code class="font-mono text-xs bg-gray-100 dark:bg-gray-700 px-1 rounded">cookies_accepted</code> de tu localStorage o limpiando las cookies del navegador. También puedes configurar tu navegador para bloquear o eliminar cookies.</p>
